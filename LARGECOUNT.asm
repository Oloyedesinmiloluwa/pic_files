;HEADER FOR 16F84
PC		EQU	2
STATUS	EQU	3
PORTA	EQU	5
PORTB	EQU	6
TMR0	EQU	1
TRISA	EQU	85H
TRISB	EQU	86H
ZEROBIT	EQU	2
COUNT	EQU	0CH
COUNT2	EQU	0DH
OPTION_R EQU	81H

	LIST P=16F84
	ORG 0
	GOTO	START

__CONFIG H'3FF0'

;SUBROUTINE SECTION
;3/32 second delay.
	DELAY CLRF TMR0 ;START TMR0.
LOOPA MOVF TMR0,W ;READ TMR0 INTO W.
	SUBLW .3 ;TIME - 3
	BTFSS STATUS, ZEROBIT ;Check TIME-W¼0
	GOTO LOOPA ;Time is not¼3.
RETLW 0 ;Time is 3, return.

DELAYP1	CLRF	TMR0
LOOPQ	MOVF	TMR0,W
	SUBLW	.3
	BTFSS	STATUS,ZEROBIT
	GOTO	LOOPQ
	RETLW	0



DELAYP25 CLRF	TMR0
LOOPB	MOVF	TMR0,W
	SUBLW	.8
	BTFSS	STATUS,ZEROBIT
	GOTO	LOOPB
	RETLW	0

DELAY1	CLRF	TMR0
LOOPC	MOVF	TMR0,W
	SUBLW	.32
	BTFSS	STATUS,ZEROBIT
	GOTO	LOOPC
	RETLW	0
DELAY125 CLRF	TMR0
LOOPD	MOVF	TMR0,W
	SUBLW	.4
	BTFSS	STATUS,ZEROBIT
	GOTO	LOOPD
	RETLW	0
DELAY2	MOVLW	.2
	MOVWF	COUNT
DEC	CALL	DELAY1
	DECFSZ	COUNT
	GOTO	DEC																						
	RETLW	0
TABLE	ADDWF	PC
		RETLW	B'00111111'	;0
		RETLW	B'00000110'	;1
		RETLW	B'01011011'	;2
		RETLW	B'01001111'	;3
		RETLW	B'01100110'	;4
		RETLW	B'01101101'	;5
		RETLW	B'01111100'	;6
		RETLW	B'00000111'	;7
		RETLW	B'01111111'	;8
		RETLW	B'01100111'	;9

	;CONFIGURATION SECTION
START	BSF	STATUS,5
	MOVLW	B'00000111'
	MOVWF	TRISA
	MOVLW	B'00000000'
	MOVWF	TRISB
	MOVLW	B'00000111'
	MOVWF	OPTION_R
	BCF	STATUS,5
	CLRF	PORTA
	CLRF	PORTB
	CLRF	COUNT
	CLRF	COUNT2
;PROGRAM STARTS; COUNT 1000
WAIT	BTFSS	PORTA,0
	GOTO	WAIT
	CALL	DELAYP1
WAIT2	BTFSC	PORTA,0
	GOTO	WAIT2
	CALL	DELAYP1	
	
	MOVLW	.256
	ADDWF	COUNT
	MOVF	COUNT,W
	SUBLW	.0
	
	BTFSS	STATUS,ZEROBIT
	GOTO	WAIT
	INCF	COUNT2
	MOVF	COUNT2,W
	SUBLW	03H
	BTFSS	STATUS,ZEROBIT
	GOTO	WAIT
WAIT01	BTFSS	PORTA,0
	GOTO	WAIT01
	CALL	DELAYP1
WAIT02	BTFSC	PORTA,0
	GOTO	WAIT02
	CALL	DELAYP1	
	MOVLW	.232
	ADDWF	COUNT
	MOVF	COUNT,W
	SUBLW	.232
	BTFSS	STATUS,ZEROBIT
	GOTO	WAIT01
	MOVLW	.8
	CALL	TABLE
	MOVWF	PORTB
	GOTO	WAIT
END



WAIT1 	MOVF TMR0,W 
		BTFSS STATUS,ZEROBIT
		GOTO WAIT1 
		INCF COUNTA
		MOVLW 25H
		SUBWF COUNTA,W 
		BTFSS STATUS,ZEROBIT 
		GOTO WAIT0 
WAIT2 	MOVF TMR0,W 
		MOVLW 80H
		SUBWF TMR0,W 
		BTFSS STATUS,ZEROBIT
		GOTO WAIT2
		BCF PORTB,0
